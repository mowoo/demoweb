			<h2>Ministry of Labor: Our Hotline Helps Victims</h2>
			<div class="left">
				<p>But an official of Ministry of Labor stressed that it’s not in charge of the prevention of sexual assault but only responsible for answering migrant workers’ reports, and that’s why it hasn’t compiled the data of legal cases. </p>

				<p>Jain-Jong Shiue, Director of Cross-Border Workforce Management Division, said that foreign victims can call the ministry’s 1955 hotline, which is a toll-free and 24 hour service, with staffs that can speak in Vietnamese, Tagalog, Thai, Indonesian, and English. Last year, the hotline answered 1143 emergent complaints in total, including sexual harassments, rapes, and so on. </p>

				<p>“Some people criticize that 1955 staff always ask victims to endure their suffering and not to call the police, but I can’t agree with it. In fact, the staff has made a huge contribution,” said Shiue.</p>

				<p>He explained that 1955 staff would immediately contact with the local labor agencies to rescue the sexual abuse victims, and provide psychological consultation and consolation money. In addition, the ministry is planning to compile the complete numbers of sexual assault cases tried in court. </p>

