			<h2>Brokers Don’t Report for Victims</h2>
			<div class="left">
				<p>Why some sexual abuse cases went unreported?</p>

				<p>First, most migrant workers in Taiwan are in debt because they have paid expensive fees to their brokers. Since they can’t afford the price of losing job, they would remain silent after being harassed or raped, in order to keep working and saving money, Wang analyzed.</p>

				<p>Second, some brokers would conceal the cases. “Female Indonesian workers are very conservative for religious reason and scared of the policemen, so they tend to make complaints to brokers instead of calling the police. Nevertheless, some brokers would verbally advise employers to stop doing it without calling the police,” said Ming-Hong Chen, a Foreign Affairs Police Officer in Keelung City. </p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿四/p02.jpg"></p>

				<p>Other brokers even threaten the victims. “Some brokers would say this to the migrant workers: are you going to report it? Be careful, because you don’t have any evidence, you will be charged with the malicious accusation and put in jail,” said Lennon Ying-Dah Wong, Director of Service Center and Shelter for Migrant Workers of Serve the People Association in Taoyuan City.  </p>
