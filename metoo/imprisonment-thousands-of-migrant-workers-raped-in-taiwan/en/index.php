				<p>By Huan-Cheng Lin, Wei Chou Chen and Liang Ju Hou</p>

				<p>Taiwan has been touting itself as “a lighthouse of human rights in Asia,” but an investigation by Apple Daily discovered that in the recent 12 years, 1048 migrant workers in the country have reported being sexually assaulted, which means it happened every four days. Moreover, some cases have gone unreported when victims remained silent, so the actual victim number could be much higher.</p>

				<p class="m"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿四/f04_1.png"></p>

				<h2>Seventy Percent of Victims Are Indonesians</h2>

				<p>Currently, there are more than 700,000 migrant workers in Taiwan, most of which come from Southeast Asia. These foreign workers contributed about 9% of the total workforce, but their basic human rights have long been neglected by Taiwanese government. </p>

				<p>According to official data, nearly 70% of the sexual abuse victims are Indonesians, with the most 727 reported cases in 12 years. Other victims are from Vietnam, Philippines and Thailand. In terms of vocation, 78% of the victims are domestic helpers or caretakers, with the most 816 cases.</p>

				<p class="m"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿四/f04_2.png"></p>

				<p>“Our government doesn’t take the issue seriously,” said Mei-Yu Wang, a member of Taiwan’s Control Yuan. When investigating on the issue, Wang found that the numbers compiled by Ministry of Health and Welfare are just “the cases reported by victims.” However, no ministry has gathered the number of the sexual abuse cases that were really tried in court.</p>

				<p>In other words, Taiwan’s Ministries of Labor, Health, and Interior haven’t followed up on or verified those reported cases, which shows their indifference toward the foreign victims.</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿四/p01.jpg"></p>

				<p>“To solve the problem, the ministries only need to integrate their data systems, and it’s not difficult at all. It seems each ministry only compiled their own data, but didn’t communicate with each other, so in the end, no one has compiled the number of legal cases,” Wang criticized.</p>
