				<p>黑數產生的另一原因，則是有些仲介會將事情「搓掉」。基隆市警局四分局外事科警員陳明宏說，印尼移工語言不通、宗教保守，且由於文化上比較威權、害怕警察，因此在遭受性侵後，他們不敢報警，而是向仲介反映。然而，有些仲介為了繼續賺取仲介費，僅會口頭勸說雇主而不報警。</p>

