			<h2>第二道高牆>>  完美受害者</h2>
			<div class="left">
				<p>此外，部分檢察官面對移工性侵案時，會套用一般性侵案的經驗法則，例如：「被害人被性侵後，應該立刻對外求援，不該繼續停留在危險的環境。」而對於離鄉背井的移工，是不是也能用相同標準？是許多NGO的共同疑問，「移工的處境裡，還存在權力不對等，也就是上下服從、優勢弱勢的問題。」律師李衣婷分析。</p>

				<p>群眾服務協會負責人汪英達，也提出同樣看法。他說，對於被性侵的移工，部分檢察官會有許多「完美被害人」的想像；而想像與想像間，卻又矛盾且缺乏同理。例如，被害人「必須」是弱小的，但他們遭遇性侵時，卻應該是積極反抗的，「他們（檢察官）常會問：『你有手機，為什麼沒求救？你行動沒受限制，為什麼不逃？』」</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿五/p02.jpg"></p>

				<p>有時，移工甚至會被質問：「你為什麼沒有攻擊他（指性侵者）？」對此汪英達解釋，移工被性侵的當下，身體被控制住，根本難以行動，「如果她做出任何傷害對方的事，可能會被嚴重攻擊、甚至遭到殺害，所以很多人在這種情況下，只會想：『我就配合你，趕快結束，不要再讓我有更多傷害了。』」</p>

				<p>律師李衣婷也表示，如果沒有符合檢察官預設的理想被害人，「（檢察官）極有可能推導出『不起訴』的結論，因為他會質疑被害人證詞的可信度。」在Alice的案件裡，檢察官便質疑她沒有離開性侵現場，也講不清被性侵幾次，導致即便有雇主的精液DNA鑑定報告、創傷後壓力症候群鑑定書，都無法起訴雇主。</p>

