			<h2>第三道高牆>> 舉證困難</h2>
			<div class="left">
				<p>「性侵害案件，本來就不容易成立。」林靜文分析，性侵發生地點通常很隱密，且多數只有加害者、被害者2人在場，「被害人一定是猝不及防，不可能自己為自己蒐證。」法務部資料也顯示，台灣每年約4000多件性侵案被通報，但因為舉證不易，起訴率僅4成出頭；若被害者限縮在移工，「（起訴率）大概剩3成，甚至只有2或1成。」</p>

				<p>林靜文指出，因為語言與文化的差異，許多移工不僅不懂如何表達自己被性侵，「也不知道該如何舉證。」有時候即使有性侵的診斷證明書，也不一定能成為證據，「因為被害人結過婚或之前有性經驗，就不見得能證明是被告造成的。」甚至有加害者還會辯駁：「她自願跟我發生性關係，並不是我強迫她。」</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿五/p04.jpg"></p>

				<p>在Alice案件裡，她保留雇主擦拭精液的衛生紙，並在後續取得精液DNA鑑定，確認精液就出自性侵她的雇主，但該案依舊不被起訴。Alice的委任律師李衣婷說，精液鑑定只能證明有性行為的有無，「但是性行為怎麼發生，是自願？是強迫？還是利用權勢？並沒有辦法從精液DNA鑑定去推論。」</p>

				<p>誠如前述，性侵案多數隱密性的犯罪，只有兩個人才知道究竟發生什麼事情，司法機關要判斷是侵害行為、還是合意，本來就比其他犯罪事件困難，李衣婷說：「也是因為這樣，我們希望有精神科的鑑定報告，來輔助司法來判斷被害人的陳述是否可信。」</p>
