				<p>文／侯良儒、陳偉周、林奐成、吳宜靜<br>「我在台灣被性侵，但這裡的司法卻給不了我正義。」想起指染自己3次的雇主未被起訴，菲籍移工Alice憤恨難平。其實，沉冤莫雪又何止Alice，法界推估，移工性侵案一旦進入司法，起訴率將只有3成不到；究其原因，語言高牆、舉證艱難，加上檢調對「完美被害人」的想像，在在讓移工對台灣的司法灰心。</p>

				<h2>第一道高牆>> 語言隔閡</h2>

				<p>「『語言』是很大的問題，」談起移工在司法裡的困境，扶助性侵案多年的律師林靜文點出第1個難處。她說，同樣是性侵案件，假使受害者是台灣人，那麼當案件進入警察體系，會有婦幼隊的女警幫忙做筆錄，到地檢署也有溫馨談話室，隔離原告與被告，「但如果是移工，他們在語言溝通上就會先遇到問題。」</p>

				<p>目前警局承接移工性侵案的流程是：員警會先通知負責外國人事務的「外事警察」，待他們聯絡翻譯人員後，再與婦幼隊一同為移工做筆錄。不過，林靜文觀察，各地的外事警察與婦幼隊，彼此間的配合並非都十分順暢，「比如找翻譯可能要等一陣子，或是先找移工的同鄉，可是這些人多數是不懂法律的。」</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿五/p01.jpg"></p>

				<p>法官王子榮提醒，司法翻譯存在一定難度，「檢察官、法官或警察訊問時，對於這些問題的理解，是需要非常多技術的成分才可以保障（被害人）。」台灣人在同樣情況下，可以找律師或尋求法律扶助，但移工只能透過通譯回答問題，「一個通譯要理解法律用語，又可以完整翻譯成被害人使用的母語，這其實非常困難。」</p>

				<p>林靜文以自己經手過的案件指出，有些移工面對檢察官詢問細節，由於與翻譯人員的溝通發生問題，無法準確描述，「導致她沒有辦法確切的指出時間、地點、犯罪行為，以至於檢察官認為你的人證、物證不齊全，因此做出不起訴處分。」</p>

				<p>司法通譯協會創辦人陳允萍分析，司法通譯是一項需要了解「語言」與「法律」的專業職能，但國內不僅沒有相關培訓制度，南亞語系翻譯人才也不足，多數狀況都是由外配或華裔充任。</p>

				<p>陳允萍說，儘管目前警政、司法機關都有編列翻譯人員名冊，但素質良莠不齊，無法準確客觀地翻譯，影響移工司法案件的判決品質。他建議，政府應建立司法通譯的證照制度，使其成為一件專業職能，而非司法辦案的工具。</p>

