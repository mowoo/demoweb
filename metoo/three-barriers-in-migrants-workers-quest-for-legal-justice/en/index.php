				<p>By Liang Ju Hou, Wei Chou Chen</p>
				<p>“I was raped in Taiwan, but I haven’t obtained any legal justice,” said Alice (pseudonym), a migrant worker interviewed by Apple Daily. This kind of case is common, lawyers familiar with the issue said, and the indictment rate of sexual abuse cases of migrant worker might be below 30%. The reasons behind it include language barrier, lack of evidence, and common stereotype of victims. </p>

				<h2>Shortage of Professional Interpreter</h2>

				<p>“Language barrier is the major problem for the migrant workers who face legal cases in Taiwan,” said Ching-Wen Lin, a lawyer working for Legal Aid Foundation and familiar with sexual abuse cases. </p>

				<p>She explained that if a foreign victim reports a case, police officer will first contact with the “Foreign Affairs Police,” who are responsible for finding interpreter to help. Then the officer will talk with the victim and make a report with the interpreter and the Women and Children Protection Brigade.</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿五/p01.jpg"></p>

				<p>“However, most interpreters can only speak in the victim’s language, but they know nothing about Taiwanese law,” Lin emphasized. </p>

				<p>Tzu Jung Wang, a judge of Yunlin District Court, stressed that it’s difficult to be a professional legal interpreter, because he or she has to understand legal terms and translate foreign language correctly at the same time. “You need to be highly skilled to protect the plaintiff when being in court,” he said.</p>

				<p>Nevertheless, there is no training system for legal interpreter in Taiwan, so the current interpreters are unprofessional foreign spouse or Chinese-speaking migrants, said Peter Chan, the establisher of Taiwan Judicial Interpreters Association.</p>

				<p>As a result, he said, although every Prosecutors Office or court works with a group of interpreters, many of them can’t translate properly and would influence the outcome of case.</p>

