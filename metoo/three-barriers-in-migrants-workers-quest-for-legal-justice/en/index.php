			<h2>Lack of Evidence</h2>
			<div class="left">
				<p>“In terms of sexual abuse case, it’s not easy to charge the perpetrator,” Ching-Wen Lin emphasized. Usually, sexual assaults take place in closed spaces such as a room, with only the perpetrator and the victim at the scene, so it’s difficult to find strong evidence to prove the criminal act.</p>

				<p>On average, according to Ministry of Justice’s data, more than 4000 sexual abuse cases are reported every year in Taiwan. Nevertheless, due to the lack of evidence, the indictment rate of sexual abuse cases of migrant worker is around 30% or even lower, Lin estimated.</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿五/p04.jpg"></p>

				<p>Because of the cultural and language barriers, many abused migrant workers don’t know how to speak out their horrible experience. In some cases, “the perpetrator would even lie that it’s a consensual sex act, but not a rape,” Lin said.</p>

				<p>Compared with other types of criminal case, it’s harder for prosecutors to make judgements of sexual abuse cases. Yi Ting Li suggested, “So, when prosecutors or judges try to determine the plausibility of victim’s testimony, the psychiatric reports should be regarded as evidence.”</p>
