			<h2>The Stereotype of “Perfect Victim”</h2>
			<div class="left">
				<p>In addition, some prosecutors handle sexual abuse cases of migrant worker with “the Rules of Experience,” and might make wrong judgements, said Yi Ting Li, a Taiwanese lawyer familiar with sexual abuse cases.</p>

				<p>“For instance, they think every sexual abuse victim would ask for help immediately, and wouldn’t stay in dangerous place. However, those assaulted migrant workers are powerless and vulnerable compared with their perpetrators, who are usually their employers,” said Li.</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿五/p02.jpg"></p>

				<p>Lennon Ying-Dah Wong, Director of Service Center and Shelter for Migrant Workers in Taoyuan City, also said that some prosecutors tend to imagine those raped foreign workers as “Perfect Victim,” which means they must be “weak but tough at the same time.”</p>

				<p>Those prosecutors would question the foreign victims: ”why didn’t you call for help or run away when being assaulted?” They don’t understand that most of foreign victims are too scared to resist against their perpetrators, Wong explained.</p>

				<p>If a foreign victim doesn’t look like “Perfect Victim,” Li analyzed, it’s possible that the perpetrator will not be charged. </p>

				<p>Take Alice’s case as an example, even the lawyer has provided evidences such as an object with perpetrator’s semen, or a medical report of post-traumatic stress disorder, the prosecutor still doubted why Alice didn’t run away and eventually decided not to charge the perpetrator. </p>

