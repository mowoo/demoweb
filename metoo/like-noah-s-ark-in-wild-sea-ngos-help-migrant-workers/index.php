			<h2>拯救最被壓迫一群人－汪英達</h2>
			<div class="left">
				<p>「不知道要花多久的時間、力氣，才能把這壓迫、奴役的制度給改變。」</p>

				<p>牆上高掛著「WE ARE WORKERS NOT SLAVES」國際NGO海報及「中止過勞」的標語；群眾服務協會（Serve the People Association, SPA）的辦公室，平均每5分鐘就有一通電話，負責人汪英達親自回復，每一通移工的求救電話。</p>

				<p>戴著黑框眼鏡、穿著樸素襯衫的汪英達，成天埋首在文件之中。他說，當年就讀人類學研究所時，同學大多是做原住民、客家族群的研究，但當時他最感興趣的，是在台灣最被壓迫的一群人：移工。</p>

				<p>後來更有一則新聞讓他淚流滿面，讓他從此走向街頭，為移工而戰。「11年前，我看到台塑六輕廠的移工新聞，移工們因為講電話爆發衝突，結果被警察送回國，太不符合比例原則。」</p>

				<p class="m"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p01.jpg"></p>

				<p>參與各項移工運動，除了實現心中的正義，也意外遇到了真愛。「我們的相遇，是在一場移工的會議上。」汪英達回憶，當時來自菲律賓的Sherry身兼多職，一面在雇主家做看護工，一面擔任菲律賓NGO團體的秘書長，為了改變同胞的命運，她積極參與活動，也因此認識汪英達，「最後成為我的老婆，更是群眾的靈魂人物。」</p>

				<p>關於移工的人道救援，夫妻倆各司其職。汪英達除了第一線接觸勞資爭議的移工，也推動移工的政策改革；Sherry則充當移工的翻譯，有時也會跟著律師南北奔波，替移工們打官司。</p>

				<p>「庇護所成立第4年，協助近800名移工，每一個都是悲傷的故事。」致力於改善移工的制度，雖然成效有限，「但我們更關心的是他們原生國貧窮的問題，有朝一日，不用再出國來當移工受苦。」</p>

