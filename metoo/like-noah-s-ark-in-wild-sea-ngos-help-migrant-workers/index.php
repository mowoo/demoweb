			<h2>打擊奴隸制度英雄－阮文雄</h2>
			<div class="left">
				<p>「移工們到台灣工作後才發現這一切都是個騙局，被剝削繼續發生。」</p>

				<p>出身越南中下階級家庭的阮文雄，從小在父母信仰的薰陶下，致力於幫助弱勢；當他成為天主教神父的那一天，決定來台替移工發聲，展開一場長達14年、與奴隸制度的抗爭。</p>

				<p>「我幫助一件100多名越南婦女集體性侵案，並開始打擊人口販運。」2004年阮文雄成立組織「越南外勞配偶辦公室」，協助約5000名越南籍移工，順利降低台灣在美國國務院「人口販賣報告」排行榜，被譽為「打擊奴隸制度」的英雄。</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p05.jpg"></p>

				<p>「背負著10幾萬的債務，逃離獨裁又貪腐的越南，來到台灣卻被性侵。」阮文雄指出，過去處理過的性侵案，移工們背債來台工作，「夢想是回越南買房子，擺脫警察的勒索。」</p>

				<p>並非每個案子都能伸張正義，「上法庭壓力大，她必須重新回憶被侵犯的過程，但結果不盡人意。」阮神父批評，檢察官只聽信雇主及其家人的證詞，「我教導她們要蒐證，最後她們因為害怕，又把錄音檔刪掉。」</p>

				<p>提及最近一件勝訴的案件，阮神父卻愁容滿面指出，「她在越南的家人竟受到仲介公司的生命威脅。」阮神父指出，自己工作也常受到威脅，這對台灣自詡為人權燈塔的形象重大打擊。</p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p06.jpg"></p>

				<p>奔走於各個移工團體，替移工爭取權益，往往在官方敷衍的態度上碰釘子。細數過去台灣的移工悲歌，從敬鵬宿、矽卡等火災意外，阮文雄砲轟保障移工的制度幾乎失靈，「政府都只是表面、形式上的處理。」</p>

				<p>《蘋果》採訪完的那天，陽光灑進庇護所的教堂，移工們朗誦著詩歌，她們感念神父在最無助的時候收留她們，願意給她們一個臨時而有溫度的家。</p>
