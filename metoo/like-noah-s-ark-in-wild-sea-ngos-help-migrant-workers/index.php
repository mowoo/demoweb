			<h2>找回人的尊嚴－陳智仁</h2>
			<div class="left">
				<p>「身為天主教徒，我們相信最重要的是『人的尊嚴』，也就是我們必須學著尊重一個人，無論她或他正面對什麼處境。」</p>

				<p>菲律賓神父Father Joy，來台16年，在台中打造的庇護所，已幫助1500名移工，「照顧勞資糾紛、肢體、口語虐待，甚至是遭性侵害的移工。」</p>

				<p>1998年Joy，首度來台灣學中文，得知這邊有很多移工遭到不公平對待，內心種下一顆福音的種子，「當我回美國後，就下定決心要來回來幫助那些移工。」</p>

				<p>Joy，不拿台灣政府的補助，全靠菲國政府金援下，在潭子區設立教堂、庇護中心及職訓中心。「剛成立新的庇護所，週日望彌撒的人潮爆滿，還排到馬路上去，熱情的台灣人也會捐贈食物跟米等物資。」</p>

				<p class="m"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p03.jpg"></p>

				<p>《蘋果》造訪那天，也來自法國志工協助陪伴移工，Joy的庇護所目前收容11名菲籍移工，包含工廠被資遣、職場工殤，甚至被性騷擾；他也替移工安排課程，從法律、職業技能及學習中文，有7成的課程是關於「心理撫慰」。</p>

				<p>「第一次上法庭，就是為了拯救一名強迫接客的菲籍移工。」Joy指出，那是一個既棘手又艱困的案件，她原本簽約要來台當看護，竟被人口販運送到苗栗一間妓院，遭囚禁6個月。</p>

				<p>該名被囚禁的移工，連上網路求救後，幾經波折，輾轉到陳智仁的庇護。「她只在我這待一晚，就被警察帶去庇護所長達8個月。」進入繁瑣的司法程序，最後終於打贏官司，將她送回菲律賓。</p>

				<p>教堂的牆上掛著一幢Joy與羅馬教宗方濟各（Pope Francis）照片，也許被賦予了使命感，Joy說，「這個庇護所的作用是幫助那些心情沮喪、沒地方睡、沒飯吃的移工，重新獲得身為一個人的尊嚴。」</p>

