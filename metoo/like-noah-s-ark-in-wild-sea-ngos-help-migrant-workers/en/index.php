			<h2>Regaining Dignity as Human Being</h2>
			<div class="left">
				<p>“As Catholics, we believe, first of all, the dignity of a person. We must learn to respect a person, regardless his or her situation,” said Father Joy, a Philippine priest and Director of Ugnayan Center in Tanzi, Taichung City. </p>

				<p>Since he came to Taiwan 16 years ago, Father Joy has helped more than 1500 migrant workers. With Philippine government’s aid, he has established a church, a shelter and a training center for the foreign workers without taking Taiwanese government’s subsidy. </p>

				<p>Every Sunday, the huge church in the downtown of Tanzi attracts dozens of Philippine migrant workers to attend the mass. Inside the church, a picture hung on the wall shows Father Joy shaking hands with Pope Francis.</p>

				<p>Currently, there are 11 Philippine workers staying in the shelter, with reasons such as sexual harassment, workplace injury, and so on. Father Joy arranges various training courses for the workers, which include Mandarin, Taiwanese law and other professional skills. In addition, psychological consoling program is also provided. </p>

				<p class="m"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p03.jpg"></p>

				<p>“The shelter’s main purpose is to help a person, especially who is in distress, to be able to regain his or her being as a person,” Father Joy stressed. </p>

