			<h2>Fighting against Slavery System</h2>
			<div class="left">
				<p>“After the migrant workers come here, they soon realize it’s a fraud and they are the victims of an exploitative system,” said Vietnamese priest Peter Nguyen, who is also the Executive Director of Vietnamese Migrant Workers & Brides Office of Catholic Church Hsinchu Diocese.</p>

				<p>In 2004, Father Nguyen established the office, helped over 100 Vietnamese victims in a sexual abuse case, and began to fight against human trafficking in Taiwan. </p>

				<p>In total, he has helped more than 5000 Vietnamese workers since he came to Taiwan 14 years ago. Moreover, his has brought changes to the exploitative system, and is praised as “a hero who fights for Vietnamese women and workers who have been trafficked” by a U.S. Department of State Trafficking in Persons Report. </p>

				<p class="m full"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p05.jpg"></p>

				<p>Father Nguyen criticized that Taiwanese government acts very slowly and passively in this issue. In the recent years, eight migrant workers have died in two separate fire accidents in Taoyuan City, which shows that the system to protect migrant workers is on the brink of a breakdown, he said.</p>

				<p>“Taiwanese government doesn’t take the issue seriously. Various types of exploitation still occur in Taiwan, so the government must face the problem and solve it,” said Father Nguyen. </p>

