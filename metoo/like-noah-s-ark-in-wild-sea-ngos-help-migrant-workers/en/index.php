			<h2>Saving the Most Oppressed People</h2>
			<div class="left">
				<p>“I have no idea how long it will take to finally change this oppressive slavery system,” said Lennon Ying-Dah Wong, Director of Service Center and Shelter for Migrant Workers of Serve the People Association in Taoyuan City.</p>

				<p>In Wong’s office, posters with slogans like “We Are Workers Not Slaves” and “End Overwork” hang on the wall, while the phone rings every five minutes. Wong always answers phone calls by himself, because he knows many of them are emergent “SOS” from migrant workers. </p>

				<p>Few years ago, a piece of news made him cry and decide to fight for migrant workers’ rights by taking to the streets. Wong recalled, “It said that several migrant workers of Formosa Plastics Group have been repatriated to their home country just because they had a brawl. It wasn’t handled properly and based on the principle of proportionality.”</p>

				<p>Later, Wong fell in love with his wife Sherry, who was a Philippine migrant worker and the secretary general of a NGO. They’ve been working together to protect the rights of oppressed foreign workers for years.</p>

				<p class="m"><img src="http://dgadobe.appledaily.com.tw/inv/2018/metoo/assets/img/ph/稿六/p01.jpg"></p>

				<p>「“Since our shelter was established four years ago, we have helped nearly 800 migrant workers, and every one of them has their own miserable story. We work hard to bring change to the oppressive system in Taiwan, but we’re more concerned about the poverty issue in their home countries. We wish that one day in future, they don’t have to go abroad to work anymore,” Wong emphasized. </p>

